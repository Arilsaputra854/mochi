# ── TRAY ICON: from bottom-right sidebar ─────────────────────────────────────
#
# Estimated tray icon region: x=1870-2240, y=1175-1530 (cat face, large)
#
tray_box = (1870, 1175, 2240, 1530)
save(tray_box, "assets/icons/tray_icon.png", size=(64, 64))
t32 = Image.open("assets/icons/tray_icon.png").resize((32,32), Image.LANCZOS)
t32.save("assets/icons/tray_icon.ico", format="ICO", sizes=[(16,16),(32,32)])
print("tray icon: saved")

# ── PREVIEW: save contact sheet for quick visual check ───────────────────────
def make_preview():
    thumbs = []
    labels = (
        [f"idle_{i}" for i in range(1,13)] +
        [f"sleeping_{i}" for i in range(1,6)] +
        [f"walk_left_{i}" for i in range(1,5)] +
        [f"walk_right_{i}" for i in range(1,5)]
    )
    for lbl in labels:
        p = f"assets/sprites/{lbl}.png"
        if os.path.exists(p):
            thumbs.append((lbl, Image.open(p)))

    cols = 8
    rows = (len(thumbs) + cols - 1) // cols
    tw, th = 72*2, 64*2
    sheet = Image.new("RGBA", (cols*tw + 10, rows*(th+20) + 10), (50,50,50,255))
    for i, (lbl, t) in enumerate(thumbs):
        col = i % cols
        row = i // cols
        t2 = t.resize((tw, th), Image.NEAREST)
        sheet.paste(t2, (col*tw + 5, row*(th+20) + 5), t2)
    sheet.save("assets/preview/contact_sheet.png")
    print(f"Preview saved: assets/preview/contact_sheet.png  ({sheet.size})")

make_preview()
print("\nDone! Check assets/preview/contact_sheet.png")
